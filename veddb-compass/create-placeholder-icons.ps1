# Create placeholder icons for VedDB Compass
# This script creates minimal valid icon files for development

$iconsDir = "icons"
New-Item -ItemType Directory -Force -Path $iconsDir | Out-Null

# Create a simple 32x32 PNG (minimal valid PNG)
$png32 = @(
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, # PNG signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, # IHDR chunk
    0x00, 0x00, 0x00, 0x20, 0x00, 0x00, 0x00, 0x20, # 32x32
    0x08, 0x02, 0x00, 0x00, 0x00, 0xFC, 0x18, 0xED, 0xA3
)
[System.IO.File]::WriteAllBytes("$iconsDir/32x32.png", $png32)

# Copy for other PNG sizes
Copy-Item "$iconsDir/32x32.png" "$iconsDir/128x128.png"
Copy-Item "$iconsDir/32x32.png" "$iconsDir/128x128@2x.png"

# Create minimal ICO file (Windows)
$ico = @(
    0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x20, 0x20,
    0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x16, 0x00, 0x00, 0x00
)
[System.IO.File]::WriteAllBytes("$iconsDir/icon.ico", $ico)

# Create minimal ICNS file (macOS)
$icns = @(
    0x69, 0x63, 0x6E, 0x73, # 'icns' magic
    0x00, 0x00, 0x00, 0x08  # file size
)
[System.IO.File]::WriteAllBytes("$iconsDir/icon.icns", $icns)

Write-Host "Placeholder icons created successfully!"
